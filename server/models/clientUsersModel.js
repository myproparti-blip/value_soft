export const authenticateUser = (clientId, username, password) => {
  if (!clientUsers[clientId]) {
    return null;
  }

  const client = clientUsers[clientId];
  if (client.admins && client.admins[username]) {
    const user = client.admins[username];
    if (user.password === password) {
      return {
        role: user.role,
        username: user.username,
        clientId: clientId,
      };
    }
  }

  if (client.managers && client.managers[username]) {
    const user = client.managers[username];
    if (user.password === password) {
      return {
        role: user.role,
        username: user.username,
        clientId: clientId,
      };
    }
  }

  if (client.users && client.users[username]) {
    const user = client.users[username];
    if (user.password === password) {
      return {
        role: user.role,
        username: user.username,
        clientId: clientId,
      };
    }
  }

  return null;
};
